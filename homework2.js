// 3 Задание
for (let a = 0; a <= 100; a++){
     // Цикл создаёт переменную а равную 0
    for (let b = 2; b <= a; b++){
         // Цикл переменной b создаёт переменную b равную 2, и на каждой итерации увеличивает на 1, пока b не станет равной a
        if (a % b == 0 && b < a) {
              // Если а делится на b без остатка и если b меньше а, то цикл b прерывается и программа переходит к следующей итерации цикла а
            break;

        } else if (b == a) {
             // Если b всё же равно а, то это значит что а не имеет делителей, кроме самого себя и 1, в результате мы получаем простое число
            console.log(a);
        }
    }
}



