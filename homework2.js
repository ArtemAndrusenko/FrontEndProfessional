let word = "hello";


for (let i = word.length - 1; i >= 0; i--) { 
    // Переменная i равная word.length - 1 означает, что i начинает свой путь с последней буквы слова;
    // i >= 0 проверяет, больше ли или равно i нулю, если да, то цикл продолжает выполнятся;
    // i-- означает, что переменная i уменьшается на 1 и что на следующей итерации цикла будет обработана предыдущая буква слова
    console.log(word[i]); 
    // Квадратные скобки используются что бы обратится к в букве в слове по индексу i, который на каждой следующей итерации цикла выводит следующую букву
}



